  def install
    # aws-lc-sys (pulled in by the Rust TLS stack) compiles its bundled CPU jitter
    # entropy source, which must be built with -O0. Homebrew's compiler shim strips
    # that -O0 and forces -O3, tripping the source's `#error` guard. Disable the
    # jitter entropy source; aws-lc falls back to the OS RNG (getentropy/getrandom).
    ENV["AWS_LC_SYS_NO_JITTER_ENTROPY"] = "1"

    virtualenv_install_with_resources
  end

